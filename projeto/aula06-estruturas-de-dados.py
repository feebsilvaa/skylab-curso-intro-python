# lista = list. Mutavel, ordenada
lista = [1, 2, 3, 4, 5]
print(type(lista))
if 2 in lista:
    print('2 esta na lista')

# tupla = tuple. Imutavel, ordenada
tupla = (1, 2, 3, 4, 5)
print(type(tupla))


# dicionario = dict
dicionario = {
    'chave': 'valor',
    'chave2': 'valor2',
    'chave3': 'valor3'
}
print(type(dicionario))

# conjunto = set
conjunto = {'Fernando', 'Miguel', 'Drika'}
print(type(conjunto))
print(conjunto)
print(conjunto['Fernando'])
